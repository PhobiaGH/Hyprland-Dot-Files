# -----------------------------------------------------------------------
# Quit running instances of Waybar.
# -----------------------------------------------------------------------
  killall waybar

# -----------------------------------------------------------------------
# Load configuration based on the current logged on user.
# -----------------------------------------------------------------------
  if [[ $USER = "$USER" ]]
  then
     waybar -c ~/.config/waybar/config & -s ~/.config/waybar/style.css
  else
     waybar &
  fi 
